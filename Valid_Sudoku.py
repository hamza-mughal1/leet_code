def poo(n1,n2):
  l = [(1,1,1),(2,1,2),(3,1,3),(1,2,4),(2,2,5),
  (3,2,6),(1,3,7),(2,3,8),(3,3,9)]
  for i in l:
    if n1/3 <= i[0] and n2/3 <= i[1]:
      return i[2] 

def foo(board):
  lis = [[] for i in range(9)]

  for pos,i in enumerate(board):
    for pos2,j in enumerate(i):
      if j != ".":
        lis[int(j)-1].append((pos2+1,pos+1,poo(pos2+1,pos+1)))
  

  for i in lis:
    for pos,j in enumerate(i):
      for k in i[pos+1:]:
        if (j[0] == k[0]) or (j[1] == k[1]) or (j[2] == k[2]):
          return False
        
  return True


